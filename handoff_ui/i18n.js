let currentLocale = "vi";

function resolveKey(locale, key) {
  return key.split(".").reduce((value, part) => (value && part in value ? value[part] : undefined), translations[locale]);
}

function formatText(template, params) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? ""));
}

function applyLocale(locale, persist = true) {
  currentLocale = SUPPORTED_LOCALES.has(locale) ? locale : "vi";
  if (typeof document !== "undefined") {
    document.documentElement.lang = currentLocale;
  }
  if (persist && typeof window !== "undefined" && window.localStorage) {
    window.localStorage.setItem(STORAGE_KEY, currentLocale);
  }
}

export function initLocale() {
  if (typeof window === "undefined") {
    return currentLocale;
  }
  const saved = window.localStorage?.getItem(STORAGE_KEY);
  const browserLocale = navigator.language?.toLowerCase().startsWith("en") ? "en" : "vi";
  applyLocale(saved && SUPPORTED_LOCALES.has(saved) ? saved : browserLocale, false);
  return currentLocale;
}

export function setLocale(locale) {
  applyLocale(locale, true);
}

export function getLocale() {
  return currentLocale;
}

export function isLocale(locale) {
  return SUPPORTED_LOCALES.has(locale);
}

export function t(key, params = {}) {
  const template = resolveKey(currentLocale, key) ?? resolveKey("vi", key) ?? key;
  return typeof template === "string" ? formatText(template, params) : template;
}
